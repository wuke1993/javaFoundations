package chap05;

public class StringIndexOf
{
    public static void main(String[] args)
    {
    	String str = "aeiou";
    	char a = 'r';
    	char b = 'o';
    	char c = 'a';
    	
    	System.out.println( str.indexOf(a) );
    	System.out.println( str.indexOf(b) );
    	System.out.println( str.indexOf(c) );
    }
}
